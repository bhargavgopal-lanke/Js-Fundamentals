// create a random number in between 1 and 100

let x1 = Math.random() * 100 + 1;

console.log("x1", Math.floor(x1));

// create a random number in between 1 and 50

let y1 = Math.random() * 50 + 1;

console.log("y1", Math.floor(y1));

const sumOutput = x1 + y1;

console.log("sumOutput", Math.floor(sumOutput));

const differenceOutput = x1 - y1;

console.log("differenceOutput", Math.floor(differenceOutput));

const productOutput = x1 * y1;

console.log("productOutput", Math.floor(productOutput));

const quotientOutput = x1 / y1;

console.log("quotientOutput", Math.floor(quotientOutput));

const remainderOutput = x1 % y1;

console.log("remainderOutput", Math.floor(remainderOutput));
